int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);
int add(int a, int b);
int sub(int a, int b);

/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int add(int a, int b) {
    return a + b;
}

/**
 * sub - Subtracts two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int sub(int a, int b) {
    return a - b;
}

/**
 * mul- Multiplies two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int mul(int a, int b) {
    return a * b;
}

/**
 * div - Divides two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int div(int a, int b) {
    return a / b;
}

/**
 * mod - Checks for the modulas of two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The result of the addition.
 */
int mod(int a, int b) {
    return a % b;
}
